#include "libft.h"

/**
 * 1.null guard
 * 2.traverse to the last node and apply func(f)
 */
void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (lst == NULL || f == NULL)
		return ;
	while (lst != NULL)
	{
		f(lst->content);
		lst = lst->next;
	}
}
